# ---------------------------------------------------------------------------
# Linear combinations  Σ_a c_a · L_a   over Z[q^±]
# ---------------------------------------------------------------------------

Label = Hashable


class Element:
    """A linear combination `Σ_a c_a · L_a` with coefficients in
    `Z[q, q^{-1}]` (= `LaurentPoly`).

    Stored as `dict[Label, LaurentPoly]` with zero LaurentPoly
    coefficients dropped on construction.

    This is the **Z-form** of the canonical basis: every distinct
    canonical-basis element gets its own label, structure constants
    are integer Laurent polynomials in `q`.  The R-module-form
    presentation is `ElementOverR`, obtained from `Element` via
    `KAlgebra.to_R_form`.

    The only involution exposed at the Element level is `bar()`
    (q-flip on each LaurentPoly coefficient).  ρ acts on labels;
    the linear extension `KAlgebra.rho_element` is plain (no twist),
    since coefficients are over Z and Z's "star" (rep-ring duality)
    is identity.
    """

    __slots__ = ("terms",)

    def __init__(
        self,
        terms: "dict[Label, LaurentPoly | RLaurent] | None" = None,
    ):
        self.terms: "dict[Label, LaurentPoly | RLaurent]" = {}
        if not terms:
            return
        for a, c in terms.items():
            if not isinstance(c, (LaurentPoly, RLaurent)):
                raise TypeError(
                    f"Element: coefficient must be LaurentPoly or "
                    f"RLaurent, got {type(c).__name__}"
                )
            if not c.is_zero():
                self.terms[a] = c

    @classmethod
    def zero(cls) -> "Element":
        return cls()

    @classmethod
    def basis(cls, label: Label) -> "Element":
        """The basis element `L_label` with coefficient 1 ∈ Z[q^±]."""
        return cls({label: LaurentPoly({0: 1})})

    def is_zero(self) -> bool:
        return not self.terms

    # ------- arithmetic -------

    def __add__(self, other: "Element") -> "Element":
        if not isinstance(other, Element):
            return NotImplemented
        out = dict(self.terms)
        for a, c in other.terms.items():
            out[a] = out[a] + c if a in out else c
        return Element({a: c for a, c in out.items() if not c.is_zero()})

    def __sub__(self, other: "Element") -> "Element":
        return self + (-other)

    def __neg__(self) -> "Element":
        return Element({a: -c for a, c in self.terms.items()})

    def __mul__(self, scalar) -> "Element":
        """Scalar multiplication by an int, LaurentPoly, or RLaurent.

        Element × Element multiplication goes through
        `KAlgebra.multiply_elements`."""
        if isinstance(scalar, int):
            if scalar == 0:
                return Element.zero()
            return Element({a: c * scalar for a, c in self.terms.items()})
        if isinstance(scalar, (LaurentPoly, RLaurent)):
            return Element({a: c * scalar for a, c in self.terms.items()})
        return NotImplemented

    __rmul__ = __mul__

    # ------- bar involution -------

    def bar(self) -> "Element":
        """q-flip on each Z[q^±] coefficient.  This is the
        KAlgebra's bar involution at the Element level."""
        out: dict[Label, "LaurentPoly | RLaurent"] = {}
        for a, c in self.terms.items():
            if isinstance(c, RLaurent):
                barred = c.bar()        # q-flip; μ untouched (bar ≠ ⋆)
            else:
                barred = LaurentPoly({-e: v for e, v in c._coeffs.items()})
            if not barred.is_zero():
                out[a] = barred
        return Element(out)

    # ------- comparison -------

    def __eq__(self, other) -> bool:
        if not isinstance(other, Element):
            return NotImplemented
        if self.terms.keys() != other.terms.keys():
            return False
        for a, c1 in self.terms.items():
            c2 = other.terms[a]
            if type(c1) is type(c2):
                if c1 != c2:
                    return False
                continue
            # Mixed LaurentPoly / RLaurent (#231 widening): compare
            # semantically by lifting the LaurentPoly into R[q^±].
            rl, lp = (c1, c2) if isinstance(c1, RLaurent) else (c2, c1)
            if not (isinstance(rl, RLaurent) and isinstance(lp, LaurentPoly)):
                return False
            R = rl.ring
            one_b = R.one_basis()
            lifted = {n: RElement(R, {one_b: c})
                      for n, c in lp._coeffs.items() if c != 0}
            if rl.coeffs != lifted:
                return False
        return True

    def __hash__(self):
        raise TypeError("Element is unhashable")

    def __repr__(self) -> str:
        if not self.terms:
            return "0"
        parts = []
        for a in sorted(self.terms.keys(), key=str):
            c = self.terms[a]
            c_repr = repr(c)
            if c_repr == "1":
                parts.append(f"L_{a}")
            elif c_repr == "-1":
                parts.append(f"-L_{a}")
            else:
                parts.append(f"({c_repr})*L_{a}")
        s = parts[0]
        for p in parts[1:]:
            if p.startswith("-"):
                s += " - " + p[1:]
            else:
                s += " + " + p
        return s


