def _coeff_min_exp(c):
    """Minimal q-exponent of an Element coefficient (LaurentPoly or
    RLaurent — both legal since the #231 widening)."""
    d = c.coeffs if isinstance(c, RLaurent) else c._coeffs
    return min(d.keys())

def _laurentpoly_times_rpowerseries(
    L: "LaurentPoly | RLaurent", P: RPowerSeries,
) -> RPowerSeries:
    """`L(q) · P(q, μ)` as an `RPowerSeries` truncated at `P.K`.

    L is either a Z[q^±]-Laurent polynomial (each integer q-coefficient
    embedded as `c · 1_R`) or an `RLaurent` over `P.ring` (R-valued
    q-coefficients used directly — `Element` admits RLaurent
    coefficients since the #231 contract widening, and the derived
    trace path must follow suit).
    """
    R = P.ring
    K = P.K
    out: dict[int, RElement] = {}
    if isinstance(L, RLaurent):
        if L.ring != R:
            raise ValueError(
                "_laurentpoly_times_rpowerseries: RLaurent ring mismatch")
        items = [(el, cl) for el, cl in L.coeffs.items()
                 if not cl.is_zero()]
        lift = lambda c: c
    else:
        items = [(el, cl) for el, cl in L._coeffs.items() if cl != 0]
        lift = lambda c: RElement(R, {R.one_basis(): c})
    for el, cl in items:
        rl = lift(cl)
        for ep, cp in P.coeffs.items():
            e = el + ep
            if e > K:
                continue
            p = rl * cp
            if p.is_zero():
                continue
            out[e] = out[e] + p if e in out else p
    return RPowerSeries(P.ring, {n: c for n, c in out.items() if not c.is_zero()}, K)


def _laurentpoly_to_rlaurent(lp: LaurentPoly, ring: ZPlusRing) -> RLaurent:
    """Lift a `LaurentPoly` (Z[q^±]) to `RLaurent` over `ring`.  Each
    integer q-coefficient embeds at `ring.one_basis()`."""
    return RLaurent(ring, dict(lp._coeffs))


