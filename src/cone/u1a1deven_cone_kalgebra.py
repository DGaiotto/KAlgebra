class U1A1DevenConeData(FiniteConeData):
    """Ray-keyed QT-cone data wrapping the self-contained `DevenConeTables`.

    Native label = `(factors, e_X01)`: `factors` a sorted tuple of
    `(ray_id, exp)`, `e_X01` the `X_{0,1}` torus power.  `X_{0,1}^В±` are the
    torus letters `E_GEN`/`E_INV`; conventional rays are integer ids."""

    def __init__(self, tables: DevenConeTables) -> None:
        self._tb = tables
        self._R = tables.R
        # cones include the X_01 torus gens (q-commute with everything).
        self._cones = tuple(frozenset(set(c) | {E_GEN, E_INV})
                            for c in tables.cones)

    # -- ConeData primitives ---------------------------------------------
    def coefficient_ring(self) -> ZPlusRing:
        return self._R

    def mult_gens(self):
        return self._tb.mult_gens

    def cones(self):
        return self._cones

    def q_commute(self, g, h) -> bool:
        return self._tb.q_commute_gen(g, h)

    def cocycle(self, g, h) -> int:
        return self._tb.cocycle_gen(g, h)

    def cross_product(self, g, h):
        return self._tb.cross_gen(g, h)

    def canonical_cone_order(self, gens):
        # ray ids (ints) before torus sentinels (tuples).
        return tuple(sorted(gens, key=_gen_sort_key))

    # -- ray-keyed label bijection (trivial) -----------------------------
    def to_cone_label(self, native):
        factors, e_X01 = native
        gens, powers = set(), {}
        for (rid, exp) in factors:
            if exp > 0:
                gens.add(rid); powers[rid] = powers.get(rid, 0) + exp
        if e_X01 > 0:
            gens.add(E_GEN); powers[E_GEN] = e_X01
        elif e_X01 < 0:
            gens.add(E_INV); powers[E_INV] = -e_X01
        return frozenset(gens), powers

    def from_cone_label(self, gens, powers):
        fac = sorted((g, powers[g]) for g in gens
                     if g not in (E_GEN, E_INV) and powers.get(g, 0) != 0)
        factors = tuple((g, e) for (g, e) in fac)
        e_X01 = powers.get(E_GEN, 0) - powers.get(E_INV, 0)
        return (factors, e_X01)

    def canonicalize_cone_label(self, cone, gens, powers):
        """Identify cone labels that are the same algebra element (the ray set
        is non-simplicial: a ray can equal a product of others Г— torus, e.g.
        `L_11 = L_10В·L_13В·X_01^{-3}`).  Canonical rep = the decode's word for
        the element's oracle section; both are the canonical basis element with
        that section, so the q-phase is 0."""
        native = self.from_cone_label(gens, powers)
        S = self._tb.oracle_section_of_native(native)
        if S is None:
            return gens, powers, 0
        canon = self._tb.native_of_oracle_section(S)
        cg, cp = self.to_cone_label(canon)
        if (cg, cp) == (gens, powers):
            return gens, powers, 0           # no redundancy вЂ” unchanged
        # L_orig == L_canon (same canonical element / oracle section), so
        #   L_{(gens,powers)} = q^{phase(canon) - phase(orig)} В· L_{(cg,cp)}.
        q_phase = self.cone_label_phase(cg, cp) - self.cone_label_phase(gens, powers)
        return cg, cp, q_phase

    # -- QTCone wiring (torus on X_{0,1}) --------------------------------
    def _torus_inverse_letter(self, g):
        if g == E_GEN:
            return E_INV
        if g == E_INV:
            return E_GEN
        return None

    def iter_cones(self):
        for cone in self._cones:
            yield Cone(self, cone, torus_gens=frozenset({E_GEN, E_INV}) & cone)

    def cycle_period_bound(self) -> int:
        return 2 * (2 * (self._tb.k or 1) + 4)


class U1A1DevenConeKAlgebra(ConeKAlgebra):
    """U(1)-gauged `[A_1, D_{2k+2}]` вЂ” self-contained ray-keyed QT-cone over
    `R(SU(2))`.  `multiply`/`ПЃ` are oracle-free closed forms; the `cв‚Ѓ = 0` trace
    seeds are **bootstrapped from `Tr(1)` alone** (`bootstrap_all_seeds`,
    spine-free) вЂ” the live `trace` runs without the oracle."""

    def __init__(self, k: int):
        if k < 1:
            raise ValueError(f"k must be >= 1, got {k}")
        self.k = k
        self._R = SU2ZPlusRing()
        # Cone tables: loaded **spine-free** from the shipped freeze
        # (`u1a1deven_tables_k{k}.pkl`) when present вЂ” construction then needs no
        # oracle (the strict-isolation release path) вЂ” else *learned* (ПЃ-folded,
        # closed-form q-powers) from the fast RG oracle and memoised per process.
        # Either way multiply/ПЃ are oracle-free closed forms (no mult-table
        # freeze: the q-powers are derived, unlike the u1a1aodd sibling).
        self._tb = build_tables(k, allow_frozen=True)
        self._oracle_ = None
        self._cone_data = U1A1DevenConeData(self._tb)
        self._seed_cache: dict = {}
        self._frozen_seeds = self._load_frozen_seeds()   # ПЃВІ-canon в†’ RPowerSeries

    @property
    def _oracle(self):
        """The RG oracle вЂ” reused from the cone tables (which built it to learn
        the mult table), so the trace seeding needs no second build."""
        if self._oracle_ is None:
            self._oracle_ = self._tb.oracle
        return self._oracle_

    def coefficient_ring(self) -> ZPlusRing:
        return self._R

    def identity(self):
        return ((), 0)

    def cone_data(self):
        return self._cone_data

    def multiply(self, a, b):
        """Self-contained cone multiply вЂ” no oracle fallback."""
        return self._multiply_via_cone_data(a, b)

    def rho(self, a):
        return self._tb.rho_native(a)

    def rho_inverse(self, a):
        return self._tb.rho_inv_native(a)

    def _canonical_rho2_orbit_rep(self, label):
        """Closed-form drift-quotient ПЃВІ-orbit rep (ПЃВІ shears the X_{0,1} torus
        в†’ infinite orbits).  Required by the generic cone Layer-1 trace
        reduction / the smart trace bootstrap."""
        return self._tb.rho2_canon(label)

    # -- trace: magnetic-charge vanishing + cached oracle seeds (cв‚Ѓ = 0) --
    def _oracle_section(self, native):
        sec = self._tb.oracle_section_of_native(native)
        if sec is None:
            raise ValueError(f"trace: {native!r} is not a cone monomial")
        return sec

    @staticmethod
    def _mag_charge(section):
        (_chord, (c1, _c2)), _ = section
        return c1

    def trace(self, a, K: int = 20):
        """Ој-refined SU(2) Schur index, **bootstrapped**: Layer-1 reduces
        `Tr(a)` to *fundamental* seeds (v-tower `X01^m` + single matter rays)
        by the generic cone ПЃВІ-twisted cyclicity (over the closed-form
        multiply), then each fundamental seed value is read off `_seed_trace`.
        `cв‚Ѓ в‰  0 в‡’ Tr = 0` (exact, per seed).  Cyclicity collapses the deep-power
        / multi-ray monomials that would otherwise need their own seed value."""
        simplified = self.cone_data().simplify_trace_via_cone_data(self, a)
        result = RPowerSeries.zero(self._R, K)
        for lab, coeff in simplified.terms.items():
            if hasattr(coeff, "is_zero") and coeff.is_zero():
                continue
            if self._mag_charge(self._oracle_section(lab)) != 0:
                continue                              # cв‚Ѓ в‰  0 в‡’ Tr = 0
            seed = self._tb.rho2_canon(lab)           # fundamental ПЃВІ-orbit rep
            if isinstance(coeff, LaurentPoly):
                coeff = RLaurent(self._R, dict(coeff._coeffs))
            emin = min(coeff.coeffs) if coeff.coeffs else 0
            wide = K - emin if emin < 0 else K
            result = result + self._seed_trace(seed, wide) * coeff
        return RPowerSeries(self._R, dict(result.coeffs), K)

    def _closed_form_gauge_trace(self, e, K):
        """`Tr(X_{0,1}^e)` вЂ” the whole **gauge sector** (vacuum `e=0` + v-tower)
        as an **exact closed-form character** (arbitrary рќ––, no oracle): the `xбµ‰`
        slice of Creutzig's `(A_1, D_{2p})` index, `p = k+1`
        (`exact_characters.deven_gauged_xn_qn`).  Cross-verified against the
        repo's INDEPENDENT `sl(3)в‚‹в‚ѓ/в‚‚` KacвЂ“Wakimoto vacuum character (theta sums)
        for `e = 0вЂ¦4` to `q_dВІвЃё`; this is what exposed the cone-oracle's
        fixed-cutoff matter-tower truncation from `q_dВІвЃґ` on."""
        from exact_characters import deven_gauged_xn_qn
        return self._qn_to_rps(deven_gauged_xn_qn(self.k, e, K), K)

    def _seed_trace(self, seed, K):
        factors, e = seed
        # GAUGE SECTOR (Tr(1) + v-tower X_{0,1}^e): exact closed-form character,
        # arbitrary рќ––, oracle-free.  Authoritative вЂ” bypasses the frozen deep
        # tail (which cached the now-superseded oracle past q_dВІВІ) and the
        # unsound matter bootstrap; never recurses.
        if not factors:
            cached = self._seed_cache.get(seed)
            if cached is not None and cached[0] >= K:
                return RPowerSeries(self._R, dict(cached[1].coeffs), K)
            series = self._closed_form_gauge_trace(e, K)
            self._seed_cache[seed] = (K, series)
            return series
        # MATTER SEED: the stand-alone SU(2) bootstrap (`matter_seed_trace`,
        # oracle-free), fed the exact closed-form gauge tower as known input.
        # Correct at arbitrary рќ–– but HEAVY beyond the frozen depth (the per-ray
        # deep-label cone reductions dominate); the frozen table serves its
        # depth instantly and the bootstrap extends it.  The standing speed item
        # is to feed exact matter seeds (per-ray module characters) so the
        # solver need not generate those rows.  Honest-fail only if the seed
        # lies outside the bootstrap's solved X01-tower range.
        fr = self._frozen_seeds.get(seed)
        if fr is not None and fr.K >= K:
            return RPowerSeries(self._R, dict(fr.coeffs), K)
        cached = self._seed_cache.get(seed)
        if cached is not None and cached[0] >= K:
            return RPowerSeries(self._R, dict(cached[1].coeffs), K)
        # The SU(2) matter bootstrap is tractable only on the small D4 (k=1) cone;
        # for k>=2 (D6+, ~3x the rays) its 30-ray deep-label reductions don't
        # scale (finding A15), so honest-fail FAST rather than hang вЂ” the
        # shipped freeze is the matter source there.
        if self._matter_bootstrap_tractable():
            qn = self.matter_seed_trace(K).get(seed)
            if qn is not None:
                series = self._qn_to_rps(qn, K)
                self._seed_cache[seed] = (K, series)
                return series
            reason = "outside the bootstrap's solved X01-tower range"
        else:
            reason = "the bootstrap is intractable for this cone size (A15); the " \
                     "shipped freeze is COMPLETE on the |m|<=10 seed box (k=2: " \
                     "221/221) but this seed lies outside it, and the W_p " \
                     "module closed forms are the open arbitrary-q item"
        raise ValueError(
            f"deven matter seed {seed!r} at рќ––^{K} is beyond the shipped freeze "
            f"(k={self.k}); {reason}")

    def _matter_bootstrap_tractable(self) -> bool:
        """Whether the spine-free SU(2) matter bootstrap is feasible.  Only on
        the small D4 (k=1) cone: k>=2's 30-ray deep-label cone reductions don't
        scale (finding A15)."""
        return self.k <= 1

    def _qn_to_rps(self, qn, K):
        """`{рќ––: {irrep: int}}` (the bootstrap's output) в†’ `RPowerSeries` over
        `R(SU(2))`."""
        coeffs = {}
        for q, d in qn.items():
            if q > K:
                continue
            re = None
            for n, c in d.items():
                term = c * self._R.basis_element(n)
                re = term if re is None else re + term
            if re is not None:
                coeffs[q] = re
        return RPowerSeries(self._R, coeffs, K)

    # -- frozen seed table (self-contained, oracle-free trace) -----------
    @staticmethod
    def _frozen_path(k):
        import os
        return os.path.join(os.path.dirname(os.path.abspath(__file__)),
                            f"u1a1deven_traces_k{k}.pkl")

    def _load_frozen_seeds(self):
        import os, pickle
        p = self._frozen_path(self.k)
        if not os.path.exists(p):
            return {}
        with open(p, "rb") as f:
            return pickle.load(f)

    def freeze_traces(self, K=16, max_chord_len=2, n_max=None):
        """Compute the ПЃВІ-canon mag-0 seed traces to order `K` (from the
        oracle) and pickle them, so later constructions read an oracle-free
        trace.  Enumerates the v-tower X_{0,1}^n and the matter rays Г— X_{0,1}^m
        up to `n_max` / `max_chord_len`.  The v-tower is the closed recursion;
        the matter families are the hard-coded ('RG answer') part."""
        import pickle
        if n_max is None:
            n_max = K + 2
        tb = self._tb
        mag0 = [i for i in range(len(tb.rays)) if tb.ray_section[i][0][1][0] == 0]
        seeds = set()
        for n in range(0, n_max + 1):
            seeds.add(((), n))                                # v-tower
        for i in mag0:
            for m in range(0, n_max + 1):
                seeds.add(tb.rho2_canon(((((i, 1),), m))))    # matter towers
        frozen = {}
        for s in seeds:
            frozen[s] = self._oracle.trace(self._oracle_section(s), K)
        with open(self._frozen_path(self.k), "wb") as f:
            pickle.dump(frozen, f)
        self._frozen_seeds = frozen
        return self._frozen_path(self.k)

    def matter_seed_trace(self, K):
        """All matter-ray seed traces via the **stand-alone SU(2) bootstrap**
        (`u1a1deven_matter_bootstrap.solve_matter`), memoised вЂ” oracle-free.

        The sole known input is the **exact closed-form gauge tower**
        (`deven_gauged_xn_qn`, fed directly via `_closed_form_gauge_trace` вЂ” no
        recursion into the matter solver, the spine-free upgrade over the old
        seed-table read); the matter rays are solved and certified per output
        irrep (ClebschвЂ“Gordan orthonormality + ПЃВІ-twisted cyclicity).  `nmax`
        (the self-norm window anchoring the sweep) starts at 3 and is grown on
        demand (two-cutoff style) until the solve certifies.  Returns
        `{matter ПЃВІ-seed: {рќ––: {irrep: int}}}`, both X01 signs.

        COST: the per-ray deep-label reductions `ray^p` (needed to deep order)
        are the bottleneck (`simplify_trace_via_cone_data` grows steeply in p),
        so this is correct but heavy beyond the frozen depth вЂ” the standing
        speed item is to *feed* exact matter seeds (the per-ray module
        characters, Г·P recipe) so the solver need not generate those rows.
        Do NOT drop rays or cap the power to go faster: the ПЃВІ-image rays are
        independent rows (same trace *value*, different tower-seed
        decomposition) and the high-p rows carry the deep reach вЂ” omitting
        either silently converges to WRONG values (learned the hard way)."""
        cache = self.__dict__.setdefault("_matter_boot", {})
