"""K-theoretic Coulomb branch algebras over `Z[q^±]` -- abstract base.

A `KAlgebra` faithfully encodes A_𝖖[T] for a 4d N=2 SQFT T:

* An associative algebra over `Z[q, q^{-1}]`.  The canonical basis
  `{L_a}` is a **Z[q, q^{-1}]-basis**: every canonical-basis element
  gets its own label in the (full) index set, and structure constants
  are integer Laurent polynomials in `q`.
* A countable linear basis `{L_a}` indexed by an opaque `Label` type.
  The identity element is one of the `L_a`'s.
* Structure constants `C^c_{ab}(q) ∈ Z[q, q^{-1}]` from
  `L_a · L_b = Σ_c C^c_{ab}(q) L_c`.
* The "bar involution" on `Z[q^±]` is **q-flip**: `q ↔ q^{-1}`.
  Constraint:  `C^c_{ab}(q^{-1}) = C^c_{ba}(q)`.  Equivalently
  `A_q ≅ A_{q^{-1}}^op` via `L_a ↔ L_a`.
* A canonical algebra automorphism `ρ` permuting the basis labels and
  fixing the identity:  `ρ(L_a) = L_{ρ(a)}`,  `ρ(1) = 1`.  Its inverse
  `ρ^{-1}` is part of the contract since cyclicity references both
  directions.
* A `ρ²`-twisted trace `Tr : A_q → R((q))` valued in a layered
  R-module structure (see below).

R-module structure as a layered API
-----------------------------------

`A_𝖖[T]` *also* carries an `R[q^±]`-module structure for `R` a Z₊-ring
(typically `R = R(G_flavour)`, the rep-ring of the flavour symmetry).
`R` sits inside `A_𝖖[T]` as the central subalgebra generated by the
canonical-basis elements at flavour directions.  The R-module
structure is **not** part of the basis definition — there is no
canonical R-basis.  It is exposed as a layered API on top of the
Z-form:

* `coefficient_ring()` returns the Z₊-ring `R`.
* `trace(a, K) : Label → RPowerSeries` lands in `R((q))` because
  the trace is non-zero only on labels in central directions, and
  picks up μ-monomials there.
* `inner_product(a, b, K) : Label × Label → RPowerSeries` likewise.
* `to_R_form(z_elem) -> ElementOverR` converts a Z-form `Element` to
  the R-form presentation (smaller index set: a section of the centre
  quotient; coefficients in `RLaurent`).  The section choice is
  internal to each realisation (via the abstract hook
  `_label_section_decompose`); it is not a contract-level concept.

Concrete subclasses must implement the **six** abstract primitives:

    coefficient_ring, identity, multiply, rho, rho_inverse, trace

plus the **flavour-lift coordinate** of a canonical label, supplied as
the single-irrep hook `r_label_decompose` (the `(section, R-basis-label)`
coordinate, `L_label = χ_w · L_section`).  As of the 2026-06-19 migration
this is **the** flavour-lift primitive; the older `(section, RElement)`
form `_label_section_decompose` is now a derived legacy bridge (default =
lift `r_label_decompose`'s basis label to an `RElement`), retained — not
deleted — because `to_R_form` reads it and it is the honest fallback for
the deprecated *emergent* flavour diagnostics (whose `r_label_decompose`
raises) and the non-contract lattice scaffolds (`coefficient_ring()` is
`None`).

Everything else (multiplication of linear combinations, the bar map
on linear combinations, the inner product, axiom verifiers,
`to_R_form`) is derived.

Module-internal dependencies: `zplus_ring` for the R-side types and
`laurent_poly` for `LaurentPoly` (the Z[q^±] type used for Element
coefficients).  Both are core modules imported by bare name.
"""

