def _section_split_via_lift(source: "KAlgebra", label):
    """`(section, source-R coefficient)` of `label`, **consuming the new
    flavour-lift coordinate** `r_label_decompose` (the single-irrep key `w` is
    lifted to the RElement `R.basis_element(w)`), and **falling back** to the
    to-be-retired `_label_section_decompose` for sources that do not yet
    implement the lift coordinate.

    The flavour-change wrappers (`_BaseChangeKAlgebra` / `_ForgetKAlgebra` /
    `_LoweredFlavourKAlgebra`) route their per-label section reads through here,
    so once every source they are applied to implements `r_label_decompose`,
    `_label_section_decompose` is no longer consumed (only the fallback path
    touches it).  For an already-migrated source the result is identical to
    `_label_section_decompose` (the forward bridge guarantees
    `_lsd = (section, R.basis_element(w))`)."""
    try:
        sec, w = source.r_label_decompose(label)
    except NotImplementedError:
        return source._label_section_decompose(label)
    return sec, source.coefficient_ring().basis_element(w)


class _BaseChangeKAlgebra(KAlgebra):
    """A KAlgebra over `phi.target` obtained by base-changing `source`
    along `phi`.  Same Label set, same multiply (Z-form, unaffected by
    R-side change); coefficient_ring / trace / inner_product / R-form
    pushed through `phi`.

    NB this is **coefficient-only** — it keeps the flavoured labels, so it is
    *not* the unflavoured algebra.  For the genuine forget (labels collapsed to
    sections, `dim`-weighted) use `KAlgebra.forget()` / `_ForgetKAlgebra`, which
    *extends* this with that collapse."""

    __slots__ = ("_source_alg", "_phi")

    def __init__(self, source_alg: KAlgebra, phi: RingHom):
        self._source_alg = source_alg
        self._phi = phi

    def coefficient_ring(self) -> ZPlusRing:
        return self._phi.target

    def identity(self) -> Label:
        return self._source_alg.identity()

    def multiply(self, a: Label, b: Label) -> Element:
        # Z-form Element is unchanged; just delegate.
        return self._source_alg.multiply(a, b)

    def rho(self, a: Label) -> Label:
        return self._source_alg.rho(a)

    def rho_inverse(self, a: Label) -> Label:
        return self._source_alg.rho_inverse(a)

    def trace(self, a: Label, K: int = 20) -> RPowerSeries:
        src_trace = self._source_alg.trace(a, K)
        return self._phi.apply_RPowerSeries(src_trace)

    def r_label_decompose(self, label: Label):
        """Flavour-lift coordinate of the base-changed algebra: the source's
        section, with its single irrep `χ_w` pushed through `φ`.

        This **consumes** the source's `r_label_decompose` and is available
        exactly when `φ` carries that irrep to a *single* target irrep
        (coefficient 1) — the flavour-growing / restriction homs
        (`unit_hom`, `tensor_inclusion_hom`, a sub-torus `restriction_hom`)
        do.  The augmentation does not (it lands on `dim(w)·χ₀`), which is why
        `forget()` (`_ForgetKAlgebra`) overrides with the section-collapse
        instead.  Raises `NotImplementedError` when the source has no lift
        coordinate, or when `φ` does not land on a single irrep."""
        sec, w_src = self._source_alg.r_label_decompose(label)
        pushed = self._phi.apply_RElement(
            self._source_alg.coefficient_ring().basis_element(w_src)
        )
        terms = [(k, c) for k, c in pushed.terms.items() if c]
        if len(terms) == 1 and terms[0][1] == 1:
            return sec, terms[0][0]
        raise NotImplementedError(
            f"_BaseChangeKAlgebra.r_label_decompose: phi sends source irrep "
            f"{w_src!r} to {dict(pushed.terms)!r}, not a single target irrep; "
            f"no single-irrep lift coordinate over phi.target."
        )

    def _label_section_decompose(
        self, label: Label,
    ) -> "tuple[Label, RElement]":
        # Section structure depends on the target R; defer to source for the
        # section labels — **consuming** the source's r_label_decompose lift
        # coordinate (falling back to the obsolete-to-be section method for
        # un-migrated sources) — and apply phi to push the source's RElement
        # coefficient through to the target ring.
        #
        # Obsolete-to-be (Plan 32): superseded by r_label_decompose above; kept
        # only for the general / virtual-character RElement form (e.g. the
        # augmentation case, where a dressed label lands on dim(w)·χ₀, which is
        # not a single irrep).
        sec, src_coef = _section_split_via_lift(self._source_alg, label)
        # phi.apply_RElement maps RElement[source_R] -> RElement[target_R].
        target_coef = self._phi.apply_RElement(src_coef)
        return sec, target_coef

    def __repr__(self) -> str:
        return f"_BaseChangeKAlgebra({self._source_alg!r}, {self._phi!r})"


def _rlaurent_over_Z_to_laurentpoly(rl: RLaurent) -> LaurentPoly:
    """`RLaurent` over `TrivialZPlusRing` → `LaurentPoly` (read off the `()`
    coefficient of each q-graded `RElement`)."""
    return LaurentPoly({
        q: c.terms.get((), 0)
        for q, c in rl.coeffs.items()
        if c.terms.get((), 0)
    })


class _ForgetKAlgebra(_BaseChangeKAlgebra):
    """The unflavoured scaffold `K_𝖖[T]` returned by `KAlgebra.forget()`:
    a **base-change along the augmentation `ε`** (handled by the
    `_BaseChangeKAlgebra` half — coefficient_ring `Z`, trace `ε∘source.trace`)
    **plus the collapse of the flavour-in-labels onto sections** (added here).
    Canonical basis = the section labels (unflavoured canonical basis);
    `multiply` = the genuine unflavoured product `ε∘to_R_form(source.multiply)`
    (`dim`-weighted on dressed inputs); `ρ` = the section-ρ.

    The unflavoured KAlgebra is produced *on demand* here, not carried; a
    subclass may override `KAlgebra.forget()` only with a genuine native
    realisation of the same theory with `G_f` forgotten (certify via
    `KAlgebraIso`) — *not* a flow's pure-gauge base.

    *Partial-forget generalisation (not built here).*  Base-changing along any
    hom `φ` compatible with augmentation (`ε_target ∘ φ = ε_source`) lowers
    `G_f` rather than forgetting it.  Unlike this `φ = ε → Z` case (a *merge*:
    `χ_r·M_s ↦ dim(r)·M_s`), a general `φ` must **re-section relative to
    `φ.target`** and can **split** canonicals where `φ` grows `Λ` (1-dim reps):
    e.g. SU(2)→U(1) sends `χ_j·M_s ↦ Σ_k μ^k·M_s = Σ_k M_{s+k}`.  So partial
    forget is a basis-changing functor, materially more than `base_change`.
    """

    __slots__ = ()

    def __init__(self, source: "KAlgebra"):
        super().__init__(source, source.coefficient_ring().augmentation())

    def _section_of(self, label: Label) -> Label:
        # Consume the source's r_label_decompose lift coordinate (fall back to
        # the obsolete-to-be section method for un-migrated sources).
        return _section_split_via_lift(self._source_alg, label)[0]

    def identity(self) -> Label:
        return self._section_of(self._source_alg.identity())

    def multiply(self, a: Label, b: Label) -> Element:
        # Z-form, over the lift coordinate (no R-form / ElementOverR): each
        # product term `c(q)·L_label` decomposes as `(section, χ_w)`, and the
        # augmentation `ε` sends `χ_w ↦ dim(w)`, contributing `dim(w)·c(q)` on
        # the section.  (Same result as `ε∘to_R_form(source.multiply)`, computed
        # per term — `to_R_form` itself only reads this lift coordinate.)
        out: "dict[Label, LaurentPoly]" = {}
        for label, lp in self._source_alg.multiply(a, b).terms.items():
            sec, r_coef = _section_split_via_lift(self._source_alg, label)
            z = self._phi.apply_RElement(r_coef).terms.get((), 0)  # ε-augmented coeff
            if z == 0:
                continue
            contrib = lp * LaurentPoly({0: z})
            out[sec] = contrib if sec not in out else out[sec] + contrib
        return Element({l: c for l, c in out.items() if not c.is_zero()})

    def rho(self, a: Label) -> Label:
        return self._section_of(self._source_alg.rho(a))

    def rho_inverse(self, a: Label) -> Label:
        return self._section_of(self._source_alg.rho_inverse(a))

    def inner_product(self, a: Label, b: Label, K: int = 20) -> RPowerSeries:
        return self._phi.apply_RPowerSeries(
            self._source_alg.inner_product(a, b, K)
        )

    def r_label_decompose(self, label: Label):
        """`forget()` is unflavoured (coefficient ring `Z`): the lift
        coordinate is trivial — section = the label itself, irrep = `χ₀`."""
        return label, self._phi.target.one_basis()

    def r_label_compose(self, section: Label, r_basis_label) -> Label:
        # Unflavoured: the only irrep is χ₀, so the label is the section.
        return section

    def _label_section_decompose(
        self, label: Label,
    ) -> "tuple[Label, RElement]":
        # Obsolete-to-be (Plan 32): derivable from r_label_decompose via the
        # forward bridge (χ₀ ↦ R.one()).  forget() is unflavoured: trivial
        # decomposition over Z.
        return label, self._phi.target.one()

    def __repr__(self) -> str:
        return f"_ForgetKAlgebra({self._source_alg!r})"


class _LoweredFlavourKAlgebra(_BaseChangeKAlgebra):
    """Flavour reduction along an augmentation-compatible hom `phi: R -> R'`
    (`KAlgebra.lower_flavour`).  Unlike `_BaseChangeKAlgebra` (coefficient-only,
    same labels) this transports the **section engine**: canonical labels are
    `(section, R'-irrep)` pairs, and the operations identify sections and push
    the R-elements through `phi` — so a canonical *splits* where `phi` grows
    `Λ`.  `_ForgetKAlgebra` is the `phi = ε` (merge) special case.

    `multiply((s1,w1),(s2,w2))` = (fuse `w1·w2` in `R'`) · `phi`(section product
    `M_{s1}·M_{s2}`), re-expanded onto `(section, R'-irrep)` labels.  `ρ` keeps
    the section-ρ and dualises the char (`⋆`).  `trace` is R-linear over the
    centre, so `Tr'((s,w)) = w·φ(source.trace(M_s))`; `inner_product` follows
    from multiply-then-trace.
    """

    __slots__ = ()

    def __init__(self, source: "KAlgebra", phi: RingHom):
        super().__init__(source, phi)

    def _section_of(self, label: Label) -> Label:
        # Consume the source's r_label_decompose lift coordinate (fall back to
        # the obsolete-to-be section method for un-migrated sources).
        return _section_split_via_lift(self._source_alg, label)[0]

    def identity(self) -> Label:
        s_id = self._section_of(self._source_alg.identity())
        return (s_id, self._phi.target.one_basis())

    def multiply(self, a: Label, b: Label) -> Element:
        # Z-form, over the lift coordinate (no R-form / ElementOverR): each
        # section-product term `c(q)·L_label` decomposes as `(s_c, χ_w)`; push
        # `χ_w` through `φ` and fuse with the input chars `w1·w2` onto
        # `(s_c, R'-irrep)` labels.  (Same result as the previous
        # `to_R_form(section product)` fold, computed per term.)
        (s1, w1), (s2, w2) = a, b
        Rp = self._phi.target
        src = self._source_alg
        w12 = Rp.multiply_basis(w1, w2)                    # {w: n}
        out: "dict[Label, LaurentPoly]" = {}
        for label, lp in src.multiply(s1, s2).terms.items():
            s_c, r_coef = _section_split_via_lift(src, label)   # (section, RElement over R)
            pushed = self._phi.apply_RElement(r_coef)          # RElement over R'
            for wi, ni in w12.items():
                for wp, npp in pushed.terms.items():
                    for wd, m in Rp.multiply_basis(wi, wp).items():
                        coeff = ni * npp * m
                        if coeff == 0:
                            continue
                        lab = (s_c, wd)
                        t = lp * LaurentPoly({0: coeff})
                        out[lab] = t if lab not in out else out[lab] + t
        return Element({l: c for l, c in out.items() if not c.is_zero()})

    def _rho_transport(self, s: Label, w, source_rho) -> Label:
        # ρ on the lowered algebra: ρ(s) generally lands on a *different
        # section* carrying an induced unit character χ (sections differ by
        # unit chars — `ρ` restricts to `⋆` on the centre).  That χ must be
        # **pushed through φ into the R′-irrep**, not dropped: the lowered
        # ρ-image is `(section(ρ s), ⋆w · φ(χ))`.  (The previous code took only
        # `section_of(ρ s)` + `⋆w`, silently discarding χ — so ρ lost the
        # charge that ρ induces on a section, e.g. on a wrapped chord.)
        Rp = self._phi.target
        s2, chi = _section_split_via_lift(self._source_alg, source_rho(s))
        pushed = self._phi.apply_RElement(chi)            # RElement over R'
        new_terms: "dict" = {}
        sw = Rp.star_basis(w)
        for wp, npp in pushed.terms.items():
            for wd, m in Rp.multiply_basis(sw, wp).items():
                new_terms[wd] = new_terms.get(wd, 0) + npp * m
        nz = {b: c for b, c in new_terms.items() if c}
        if len(nz) != 1 or next(iter(nz.values())) != 1:
            raise ValueError(
                "lower_flavour.rho: ρ-induced character is not a single unit "
                f"(got {nz}); ρ is not a label permutation on this reduction")
        return (s2, next(iter(nz)))

    def rho(self, a: Label) -> Label:
        s, w = a
        return self._rho_transport(s, w, self._source_alg.rho)

    def rho_inverse(self, a: Label) -> Label:
        s, w = a
        return self._rho_transport(s, w, self._source_alg.rho_inverse)

    def embed_R(self, r: RElement) -> Element:
        Rp = self._phi.target
        if not isinstance(r, RElement) or r.ring != Rp:
            raise TypeError("embed_R: argument must be an RElement over R'")
        s_id = self._section_of(self._source_alg.identity())
        out: "dict[Label, LaurentPoly]" = {}
        for w, n in r.terms.items():
            if n == 0:
                continue
            lab = (s_id, w)
            t = LaurentPoly({0: n})
            out[lab] = t if lab not in out else out[lab] + t
        return Element({l: c for l, c in out.items() if not c.is_zero()})

    def r_label_decompose(self, label: Label):
        """Lift coordinate of the lowered algebra.  Its labels are already
        `(section, R'-irrep)` pairs `(s, w)`, so the coordinate is read off
        directly: section `(s, χ₀)` (the same section dressed by the trivial
        R'-irrep), single irrep `w`."""
        s, w = label
        return (s, self._phi.target.one_basis()), w

    def r_label_compose(self, section: Label, r_basis_label) -> Label:
        # Inverse of r_label_decompose: re-dress the section's gauge part by w.
        (s, _one) = section
        return (s, r_basis_label)

    def _label_section_decompose(
        self, label: Label,
    ) -> "tuple[Label, RElement]":
        # Obsolete-to-be (Plan 32): derivable from r_label_decompose via the
        # forward bridge ((s,w) ↦ ((s,χ₀), R'.basis_element(w))).
        s, w = label
        Rp = self._phi.target
        return (s, Rp.one_basis()), Rp.basis_element(w)

    def trace(self, a: Label, K: int = 20) -> RPowerSeries:
        # `Tr` is R-linear over the central embedding, so it is fully determined
        # by its value on sections: `Tr((s, χ)) = χ·Tr(M_s)`.  Transport:
        # `Tr'((s, w)) = w · φ(source.trace(M_s))`.  (`inner_product` then
        # follows from the default multiply-then-trace.)
        s, w = a
        Rp = self._phi.target
        pushed = self._phi.apply_RPowerSeries(self._source_alg.trace(s, K))
        w_elt = Rp.basis_element(w)
        coeffs = {q: w_elt * relem for q, relem in pushed.coeffs.items()}
        return RPowerSeries(
            Rp, {q: c for q, c in coeffs.items() if not c.is_zero()}, pushed.K,
        )

    def __repr__(self) -> str:
        return f"_LoweredFlavourKAlgebra({self._source_alg!r}, {self._phi!r})"
