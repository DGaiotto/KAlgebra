# ElementOverR — the R-module-form presentation
# ---------------------------------------------------------------------------


class ElementOverR:
    """The R-form presentation of an Element: `dict[Label, RLaurent]`
    with R-Laurent coefficients over a Z₊-ring `R`.

    Returned by `KAlgebra.to_R_form(z_elem)`.  Index labels are
    section reps of the central quotient; flavour shifts are
    absorbed into μ-monomial coefficients.  Useful when the user
    wants the smaller R-module-basis presentation (e.g., for
    refined Schur indices, real Schur quantization).

    The R-form is *not* the canonical basis — it is a derived view
    that depends on the realisation's section choice.
    """

    __slots__ = ("ring", "terms")

    def __init__(
        self,
        ring: ZPlusRing,
        terms: dict[Label, RLaurent] | None = None,
    ):
        self.ring = ring
        self.terms: dict[Label, RLaurent] = {}
        if not terms:
            return
        for a, c in terms.items():
            if not isinstance(c, RLaurent):
                raise TypeError(
                    f"ElementOverR: coefficient must be RLaurent, "
                    f"got {type(c).__name__}"
                )
            if c.ring != ring:
                raise ValueError("ElementOverR: ring mismatch in coefficient")
            if not c.is_zero():
                self.terms[a] = c

    @classmethod
    def zero(cls, ring: ZPlusRing) -> "ElementOverR":
        return cls(ring)

    @classmethod
    def basis(cls, ring: ZPlusRing, label: Label) -> "ElementOverR":
        return cls(ring, {label: RLaurent.one(ring)})

    def is_zero(self) -> bool:
        return not self.terms

    def __add__(self, other: "ElementOverR") -> "ElementOverR":
        if not isinstance(other, ElementOverR):
            return NotImplemented
        if other.ring != self.ring:
            raise ValueError("ElementOverR: ring mismatch in __add__")
        out = dict(self.terms)
        for a, c in other.terms.items():
            out[a] = out[a] + c if a in out else c
        return ElementOverR(
            self.ring,
            {a: c for a, c in out.items() if not c.is_zero()},
        )

    def __sub__(self, other: "ElementOverR") -> "ElementOverR":
        return self + (-other)

    def __neg__(self) -> "ElementOverR":
        return ElementOverR(
            self.ring, {a: -c for a, c in self.terms.items()},
        )

    def __eq__(self, other) -> bool:
        if not isinstance(other, ElementOverR):
            return NotImplemented
        return self.ring == other.ring and self.terms == other.terms

    def __hash__(self):
        raise TypeError("ElementOverR is unhashable")

    def __repr__(self) -> str:
        if not self.terms:
            return "0_R"
        parts = []
        for a in sorted(self.terms.keys(), key=str):
            c = self.terms[a]
            parts.append(f"({c})*L_{a}")
        return " + ".join(parts)


